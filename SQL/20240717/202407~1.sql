-- TRIGGER 트리거
CREATE OR REPLACE TRIGGER TRIGGER_TEST1
    BEFORE -- AFTER
    UPDATE ON DEPT
    FOR EACH ROW
BEGIN
    DBMS_OUTPUT.PUT_LINE('변경 전 : ' || :OLD.DNAME);
    DBMS_OUTPUT.PUT_LINE('변경 전 : ' || :NEW.DNAME);
END;
/

SET SERVEROUTPUT ON;
UPDATE DEPT
SET 
    DNAME = 'TEST'
WHERE
    DEPTNO = 10;
    
