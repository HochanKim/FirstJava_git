CREATE OR REPLACE TRIGGER TRIGGER_TEST2
    BEFORE
    INSERT ON EMP
    FOR EACH ROW
DECLARE  -- TRIGGER는 'DECLARE'로 변수선언
    SALAVG EMP.SAL%TYPE;
BEGIN
    SELECT ROUND(AVG(SAL), 0)
    INTO SALAVG
    FROM EMP;
    DBMS_OUTPUT.PUT_LINE('급여 평균 : ' || SALAVG);
END;
/

